package punto2;

public class Destino {
    private String nombre;

    /*Constructor*/
    public Destino(String nombre){
        setNombre(nombre);
    }

    /*Setters*/
    public void setNombre(String destino) {
        this.nombre = destino;
    }

    /*Getters*/
    public String getNombre() {
        return nombre;
    }

}
